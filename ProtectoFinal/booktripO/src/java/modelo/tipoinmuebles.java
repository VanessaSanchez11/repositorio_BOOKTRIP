package modelo;

public class tipoinmuebles {

    public int idTipo;
    public String nombre;

    public void setIdTipo(int idTipo) {
        this.idTipo = idTipo;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getIdTipo() {
        return idTipo;
    }

    public String getNombre() {
        return nombre;
    }

}
